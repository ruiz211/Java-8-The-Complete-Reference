package com.ruiz211.TheJavaLanguage.V.ControlStatements;

public class Ifs {
    public static void main(String[] args) {
        int a = 10;
        int b = 11;

        if (a > b) System.out.println("a");
        else System.out.println("b");

        System.out.println((a > b) ? "a" : "b");
    }
}
